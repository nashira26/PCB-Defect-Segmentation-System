def load_multi_class_data():
    """Load data with proper multi-class segmentation mapping"""
    all_images = []
    all_masks = []

    # Create class mapping: folder name -> class index
    class_to_idx = {cls: i+1 for i, cls in enumerate(CLASSES)}

    for class_name in CLASSES:
        img_dir = os.path.join(IMAGE_BASE_DIR, class_name)
        mask_dir = os.path.join(MASK_BASE_DIR, class_name)

        class_idx = class_to_idx[class_name]

        # Get all image files
        for img_file in os.listdir(img_dir):
            if not img_file.lower().endswith(('.png', '.jpg', '.jpeg')):
                continue

            img_path = os.path.join(img_dir, img_file)

            # Try to find corresponding mask
            mask_path = os.path.join(mask_dir, img_file)
            if not os.path.exists(mask_path):
                # Try different extensions
                base_name = os.path.splitext(img_file)[0]
                for ext in ['.png', '.jpg', '.jpeg', '.PNG', '.JPG', '.JPEG']:
                    alt_path = os.path.join(mask_dir, base_name + ext)
                    if os.path.exists(alt_path):
                        mask_path = alt_path
                        break

            if not os.path.exists(mask_path):
                continue

            all_images.append(img_path)
            all_masks.append((mask_path, class_idx))

    print(f"Total image-mask pairs: {len(all_images)}")

    # Analyze defect sizes
    print("\n Defect size analysis:")
    for class_idx, class_name in enumerate(CLASSES, 1):
        class_masks = [m for m in all_masks if m[1] == class_idx]
        if class_masks:
            defect_sizes = []
            for mask_path, _ in class_masks[:10]:  # Sample 10 masks
                mask = cv2.imread(mask_path, cv2.IMREAD_GRAYSCALE)
                if mask is not None:
                    defect_pixels = np.sum(mask > 127)
                    total_pixels = mask.shape[0] * mask.shape[1]
                    defect_sizes.append(defect_pixels / total_pixels * 100)

            if defect_sizes:
                avg_size = np.mean(defect_sizes)
                min_size = np.min(defect_sizes)
                max_size = np.max(defect_sizes)
                print(f"  {class_name}: avg {avg_size:.4f}%, range {min_size:.4f}%-{max_size:.4f}%")

    return all_images, all_masks, class_to_idx

