def calculate_class_weights_for_loss():
    """Calculate weights: background=1, defects=high weight"""
    # Based on your 99.96% background, 0.04% defects ratio
    background_weight = 1.0

    # Defects need MUCH higher weight to counter imbalance
    # If defects are 0.04% of pixels, weight should be ~2500x
    # But we'll cap at 200 to avoid numerical instability
    defect_weight = 200.0  # Increased from 100 to 200

    class_weights = [background_weight] + [defect_weight] * len(CLASSES)

    print(f"\n Class weights for loss function:")
    print(f"  Background: {background_weight}")
    for i, cls in enumerate(CLASSES):
        print(f"  {cls}: {defect_weight}")

    return class_weights

# Calculate class weights BEFORE defining loss functions
CLASS_WEIGHTS = calculate_class_weights_for_loss()

def weighted_categorical_crossentropy(weights):
    """Custom loss with class weights - MORE AGGRESSIVE"""
    weights_tensor = tf.constant(weights, dtype=tf.float32)

    def loss(y_true, y_pred):
        # Clip predictions to avoid log(0)
        y_pred = tf.clip_by_value(y_pred, 1e-7, 1.0 - 1e-7)

        # Calculate weighted categorical crossentropy
        # More weight on defect pixels
        loss_per_pixel = -tf.reduce_sum(y_true * tf.math.log(y_pred) * weights_tensor, axis=-1)

        # Return mean loss
        return tf.reduce_mean(loss_per_pixel)

    return loss

def dice_loss_multi_class(y_true, y_pred, smooth=1e-6):
    """Dice loss for multi-class segmentation - BETTER FOR IMBALANCE"""
    # Flatten
    y_true_f = tf.reshape(y_true, [-1, NUM_CLASSES])
    y_pred_f = tf.reshape(y_pred, [-1, NUM_CLASSES])

    # Calculate intersection and union
    intersection = tf.reduce_sum(y_true_f * y_pred_f, axis=0)
    total = tf.reduce_sum(y_true_f, axis=0) + tf.reduce_sum(y_pred_f, axis=0)

    # Dice coefficient per class
    dice_per_class = (2. * intersection + smooth) / (total + smooth)

    # Focus more on defect classes by weighting
    defect_weight = 2.0  # Give defects 2x more importance in Dice
    class_weights_dice = tf.constant([1.0] + [defect_weight] * len(CLASSES), dtype=tf.float32)

    weighted_dice = dice_per_class * class_weights_dice
    mean_dice = tf.reduce_mean(weighted_dice)

    return 1.0 - mean_dice

def combined_weighted_loss(y_true, y_pred):
    """Combine weighted CE and Dice loss - OPTIMIZED FOR DEFECTS"""
    # Weighted categorical crossentropy (heavily weights defects)
    wce_loss = weighted_categorical_crossentropy(CLASS_WEIGHTS)(y_true, y_pred)

    # Dice loss (good for segmentation boundaries)
    dice_loss_val = dice_loss_multi_class(y_true, y_pred)

    # Use more CE (70%) to focus on class imbalance, less Dice (30%)
    return 0.7 * wce_loss + 0.3 * dice_loss_val

def mean_iou_multi_class(y_true, y_pred):
    """Mean IoU for multi-class segmentation"""
    # Threshold predictions
    y_pred = tf.argmax(y_pred, axis=-1)
    y_pred = tf.one_hot(tf.cast(y_pred, tf.int32), NUM_CLASSES)

    # Calculate intersection and union for each class
    intersection = tf.reduce_sum(y_true * y_pred, axis=[1, 2])
    union = tf.reduce_sum(y_true, axis=[1, 2]) + tf.reduce_sum(y_pred, axis=[1, 2]) - intersection

    # Calculate IoU and average
    iou_per_class = (intersection + 1e-7) / (union + 1e-7)
    return tf.reduce_mean(iou_per_class)

def categorical_accuracy_multi_class(y_true, y_pred):
    """Categorical accuracy for segmentation"""
    # Convert predictions to class indices
    y_pred = tf.argmax(y_pred, axis=-1)
    y_true = tf.argmax(y_true, axis=-1)

    # Calculate accuracy
    equal = tf.cast(tf.equal(y_pred, y_true), tf.float32)
    return tf.reduce_mean(equal)

def defect_only_iou(y_true, y_pred):
    """IoU for DEFECT CLASSES ONLY (excludes background)"""
    # Threshold predictions
    y_pred = tf.argmax(y_pred, axis=-1)
    y_pred = tf.one_hot(tf.cast(y_pred, tf.int32), NUM_CLASSES)

    # Focus only on defect classes (skip background class 0)
    y_true_defects = y_true[..., 1:]
    y_pred_defects = y_pred[..., 1:]

    # Calculate intersection and union for defect classes
    intersection = tf.reduce_sum(y_true_defects * y_pred_defects, axis=[1, 2])
    union = tf.reduce_sum(y_true_defects, axis=[1, 2]) + tf.reduce_sum(y_pred_defects, axis=[1, 2]) - intersection

    # Calculate IoU for defect classes
    iou_defects = tf.where(union > 0, intersection / union, tf.zeros_like(intersection))
    return tf.reduce_mean(iou_defects)

