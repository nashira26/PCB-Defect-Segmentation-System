# BUILD U-NET FOR MULTI-CLASS SEGMENTATION

def build_multi_class_unet(input_size=256, num_classes=7):
    """Build U-Net for multi-class semantic segmentation"""
    inputs = layers.Input(shape=(input_size, input_size, 3))

    # Encoder
    c1 = layers.Conv2D(64, 3, padding='same', activation='relu')(inputs)
    c1 = layers.Conv2D(64, 3, padding='same', activation='relu')(c1)
    p1 = layers.MaxPooling2D((2, 2))(c1)

    c2 = layers.Conv2D(128, 3, padding='same', activation='relu')(p1)
    c2 = layers.Conv2D(128, 3, padding='same', activation='relu')(c2)
    p2 = layers.MaxPooling2D((2, 2))(c2)

    c3 = layers.Conv2D(256, 3, padding='same', activation='relu')(p2)
    c3 = layers.Conv2D(256, 3, padding='same', activation='relu')(c3)
    p3 = layers.MaxPooling2D((2, 2))(c3)

    c4 = layers.Conv2D(512, 3, padding='same', activation='relu')(p3)
    c4 = layers.Conv2D(512, 3, padding='same', activation='relu')(c4)
    p4 = layers.MaxPooling2D((2, 2))(c4)

    # Bottleneck
    c5 = layers.Conv2D(1024, 3, padding='same', activation='relu')(p4)
    c5 = layers.Conv2D(1024, 3, padding='same', activation='relu')(c5)
    c5 = layers.Dropout(0.5)(c5)

    # Decoder
    u6 = layers.Conv2DTranspose(512, 2, strides=2, padding='same')(c5)
    u6 = layers.concatenate([u6, c4])
    c6 = layers.Conv2D(512, 3, padding='same', activation='relu')(u6)
    c6 = layers.Conv2D(512, 3, padding='same', activation='relu')(c6)

    u7 = layers.Conv2DTranspose(256, 2, strides=2, padding='same')(c6)
    u7 = layers.concatenate([u7, c3])
    c7 = layers.Conv2D(256, 3, padding='same', activation='relu')(u7)
    c7 = layers.Conv2D(256, 3, padding='same', activation='relu')(c7)

    u8 = layers.Conv2DTranspose(128, 2, strides=2, padding='same')(c7)
    u8 = layers.concatenate([u8, c2])
    c8 = layers.Conv2D(128, 3, padding='same', activation='relu')(u8)
    c8 = layers.Conv2D(128, 3, padding='same', activation='relu')(c8)

    u9 = layers.Conv2DTranspose(64, 2, strides=2, padding='same')(c8)
    u9 = layers.concatenate([u9, c1])
    c9 = layers.Conv2D(64, 3, padding='same', activation='relu')(u9)
    c9 = layers.Conv2D(64, 3, padding='same', activation='relu')(c9)

    # Output layer for multi-class segmentation (softmax for single class per pixel)
    outputs = layers.Conv2D(num_classes, 1, activation='softmax')(c9)

    model = Model(inputs=inputs, outputs=outputs, name='MultiClass_UNet')
    return model

