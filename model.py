class MultiClassSegmentationGenerator(tf.keras.utils.Sequence):
    def __init__(self, *args, **kwargs):
        # Add defect-focused sampling
        self.defect_focus_prob = 0.7  # 70% chance to sample defect-containing images
        super().__init__(*args, **kwargs)

    def on_epoch_end(self):
        # FOCUS ON DEFECT-CONTAINING IMAGES
        self.balanced_indices = []

        # Get indices of images WITH defects (class_idx > 0)
        defect_indices = []
        for class_idx in range(1, self.num_classes):  # Skip background
            defect_indices.extend(self.class_indices[class_idx])

        # Remove duplicates
        defect_indices = list(set(defect_indices))

        # If we have defect images, oversample them
        if defect_indices:
            # Calculate how many defect samples to include (70% of batch)
            n_defect_samples = int(len(defect_indices) * 0.7)
            if n_defect_samples > len(defect_indices):
                # Repeat if needed
                repeats = n_defect_samples // len(defect_indices) + 1
                sampled_defects = defect_indices * repeats
                sampled_defects = sampled_defects[:n_defect_samples]
            else:
                sampled_defects = np.random.choice(defect_indices,
                                                  n_defect_samples,
                                                  replace=False)

            # Add background images for remaining 30%
            background_needed = self.batch_size - len(sampled_defects)
            all_indices = list(range(len(self.image_paths)))
            background_indices = np.random.choice(all_indices,
                                                 background_needed,
                                                 replace=True)

            self.balanced_indices = list(sampled_defects) + list(background_indices)
        else:
            # Fallback to all images
            self.balanced_indices = list(range(len(self.image_paths)))

        if self.shuffle:
            np.random.shuffle(self.balanced_indices)

        self.balanced_indices = np.array(self.balanced_indices)

    def apply_augmentation(self, image, mask):
        """ENHANCED AUGMENTATION FOR TINY DEFECTS"""
        # Store defect location before augmentation
        defect_pixels = np.sum(mask > 127)

        # Random flip
        if np.random.random() > 0.5:
            image = np.fliplr(image)
            mask = np.fliplr(mask)

        # Random rotation - SMALL angles to preserve defect integrity
        if np.random.random() > 0.5:
            angle = np.random.uniform(-15, 15)  # Small rotations
            h, w = image.shape[:2]
            center = (w//2, h//2)

            # Rotate image
            M = cv2.getRotationMatrix2D(center, angle, 1.0)
            image = cv2.warpAffine(image, M, (w, h),
                                  flags=cv2.INTER_LINEAR,
                                  borderMode=cv2.BORDER_REFLECT)

            # Rotate mask with NEAREST interpolation
            mask = cv2.warpAffine(mask, M, (w, h),
                                 flags=cv2.INTER_NEAREST,
                                 borderMode=cv2.BORDER_CONSTANT,
                                 borderValue=0)

        # DEFECT-SPECIFIC AUGMENTATIONS:
        # 1. CLAHE for local contrast enhancement
        if np.random.random() > 0.5 and defect_pixels > 0:
            # Apply CLAHE only around defect region
            lab = cv2.cvtColor(image, cv2.COLOR_RGB2LAB)
            l, a, b = cv2.split(lab)
            clahe = cv2.createCLAHE(clipLimit=3.0, tileGridSize=(8,8))
            l = clahe.apply(l)
            lab = cv2.merge([l, a, b])
            image = cv2.cvtColor(lab, cv2.COLOR_LAB2RGB)

        # 2. Selective sharpening to enhance defect edges
        if np.random.random() > 0.5 and defect_pixels > 0:
            kernel = np.array([[-1,-1,-1],
                              [-1, 9,-1],
                              [-1,-1,-1]])
            image = cv2.filter2D(image, -1, kernel)

        # 3. Random zoom/crop to make defects larger in context
        if np.random.random() > 0.3 and defect_pixels > 0:
            # Find defect bounding box
            coords = np.column_stack(np.where(mask > 127))
            if len(coords) > 0:
                y1, x1 = coords.min(axis=0)
                y2, x2 = coords.max(axis=0)

                # Zoom in around defect (crop and resize)
                zoom_factor = np.random.uniform(1.5, 2.5)
                h, w = image.shape[:2]

                # Calculate crop around defect
                defect_center = ((y1+y2)//2, (x1+x2)//2)
                crop_size = min(h, w) // 2  # Crop half the image size

                y_start = max(0, defect_center[0] - crop_size//2)
                y_end = min(h, defect_center[0] + crop_size//2)
                x_start = max(0, defect_center[1] - crop_size//2)
                x_end = min(w, defect_center[1] + crop_size//2)

                # Crop and resize back
                if y_end > y_start and x_end > x_start:
                    image_crop = image[y_start:y_end, x_start:x_end]
                    mask_crop = mask[y_start:y_end, x_start:x_end]

                    image = cv2.resize(image_crop, (w, h),
                                     interpolation=cv2.INTER_LINEAR)
                    mask = cv2.resize(mask_crop, (w, h),
                                    interpolation=cv2.INTER_NEAREST)

        # 4. Noise addition ONLY to background (simulate realistic conditions)
        if np.random.random() > 0.7:
            # Add slight noise to entire image
            noise = np.random.randn(*image.shape) * 10
            image = np.clip(image + noise, 0, 255)

        return image, mask

